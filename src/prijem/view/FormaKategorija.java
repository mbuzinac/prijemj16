  protected void ucitaj() {
        rezultati=HibernateUtil.getSession().createQuery("from Kategorija a where a.obrisan=false").list();
        ucitavanje();
    }
    
    private void ucitavanje(){
        DefaultListModel<Kategorija> m = new DefaultListModel<>();
        lstKategorije.setModel(m);
        rezultati.forEach((s) -> {
            m.addElement(s);
        });
        
       
    }

    
