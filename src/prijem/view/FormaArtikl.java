    private void listaValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listaValueChanged
           
        if (evt.getValueIsAdjusting()){
            return;
        }

        try {
            this.entitet=lista.getSelectedValue();
            cmbKategorije.setSelectedItem(entitet.getKategorija());
            cmbDobavljaci.setSelectedItem(entitet.getDobavljac());
            this.txtNaziv.setText(entitet.getNaziv());
            this.txtEan.setText(entitet.getEan());
            this.txtPakiranje.setText(entitet.getPakiranje());
            this.txtCijena.setText(lista.getSelectedValue().getCijena().toString());
            
            

        } catch (Exception e) {
        }
    }//GEN-LAST:event_listaValueChanged

