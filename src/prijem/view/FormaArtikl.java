    private void ucitajKategorija() {
        DefaultComboBoxModel<Kategorija> m = new DefaultComboBoxModel<>();
        cmbKategorije.setModel(m);
        List<Kategorija> naziv = HibernateUtil.getSession().
                createQuery("from Kategorija a where "
                        + "a.obrisan=false  ").list();
   for (Kategorija p : naziv) {

            m.addElement(p);
            cmbKategorije.setSelectedItem(p);

        }
    }
    private void ucitajDobavljac() {
         DefaultComboBoxModel<Dobavljac> m = new DefaultComboBoxModel<>();
        cmbDobavljaci.setModel(m);
        List<Dobavljac> naziv = HibernateUtil.getSession().
                createQuery("from Dobavljac a where "
                        + "a.obrisan=false  ").list();
        for (Dobavljac p : naziv) {

            m.addElement(p);
            cmbDobavljaci.setSelectedItem(p);
   
        }

}
